from gtts import gTTS
import os

BANGLA_CONVERSATION = """
ডাক্তার: সালাম, বসুন। আপনার কী সমস্যা?
রোগী: সালাম ডাক্তার সাহেব। আমার বুকে ব্যথা এবং কাশি হচ্ছে।
ডাক্তার: কতদিন ধরে এই সমস্যা হচ্ছে?
রোগী: প্রায় তিন দিন ধরে।
ডাক্তার: আপনার বয়স কত?
রোগী: আমার বয়স বাইশ বছর।
ডাক্তার: আপনার নাম কী?
রোগী: আমার নাম রবিউল আবেদিন।
ডাক্তার: আপনার ফোন নম্বর কত?
রোগী: আমার ফোন নম্বর হলো শূন্য এক সাত পাঁচ তিন চার সাত ছয় আট।
ডাক্তার: আপনি কি ধূমপান করেন?
রোগী: না ডাক্তার সাহেব, আমি ধূমপান করি না।
ডাক্তার: আপনার বুকে কি শব্দ হচ্ছে?
রোগী: হ্যাঁ, কখনো কখনো শব্দ হয়।
ডাক্তার: আমি মনে করছি আপনার ব্রঙ্কাইটিস হয়েছে। আপনাকে একটি এক্স-রে করতে হবে।
রোগী: ঠিক আছে ডাক্তার সাহেব।
ডাক্তার: এছাড়াও আপনাকে কিছু ওষুধ দেব। ব্যাকটেরিয়াল ইনফেকশনের জন্য অ্যাজিথ্রোমাইসিন এবং কাশির জন্য সিরাপ খাবেন।
রোগী: ধন্যবাদ ডাক্তার সাহেব।
ডাক্তার: আপনি চার থেকে পাঁচ দিনের মধ্যে আবার আসবেন।
রোগী: ঠিক আছে, ধন্যবাদ।
"""

tts = gTTS(text=BANGLA_CONVERSATION, lang='bn')
tts.save("sample_conversation.mp3")

print("Audio file created: sample_conversation.mp3")
print(f"File size: {os.path.getsize('sample_conversation.mp3')} bytes")